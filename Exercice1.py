# Déclaration des variables avec différents types
x = 10 # x est un entier (int)
y = 3.14 # y est un nombre à virgule flottante (float)
z = "Bonjour" # z est une chaîne de caractères (str)
a = True # a est une valeur booléenne (bool)

# Affichage du type de chaque variable
print(f"x: {type(x)}")
print(f"y: {type(y)}")
print(f"z: {type(z)}")
print(f"a: {type(a)}")