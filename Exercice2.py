# Demande à l'utilisateur d'entrer un nombre et stocke l'entrée sous forme de chaîne de caractères
nombre = input("Entrez un nombre : ")
# Convertit la chaîne de caractères en un entier et stocke le résultat dans la variable nombre_entier
nombre_entier = int(nombre)
# Convertit la chaîne de caractères en un flottant et stocke le résultat dans la variable nombre_flottant
nombre_flottant = float(nombre)

print(f"Nombre entier : {nombre_entier}")
print(f"Nombre flottant : {nombre_flottant}")